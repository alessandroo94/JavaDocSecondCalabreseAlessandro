/**
 * this class is used to do calculations
 * @author Alessandro Calabese
 */
public class ArithmeticOperators {

    /**
     * sum method return the summation of twe number
     * @param num1 the first input
     * @param num2 the last input
     * @return
     */
    public int  sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * sub method return the subtraction of twe number
     * @param num1 the first input
     * @param num2 the last input
     * @return
     */
    public  int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * mul method return the multiplication of twe number
     * @param num1 the first input
     * @param num2 the last input
     * @return
     */
    public  int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * div method return the division of twe number
     * @param num1 the first input
     * @param num2 the last input
     * @return
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
